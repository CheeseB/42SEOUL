int	ft_str_is_numeric(char *str)
{
	int	idx;

	idx = 0;
	while (str[idx] != '\0')
	{
		if (str[idx] < 48 || str[idx] > 57)
		{
			return (0);
		}
		idx++;
	}
	return (1);
}
