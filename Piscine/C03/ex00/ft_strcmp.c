int	ft_strcmp(char *s1, char *s2)
{
	int	sub;

	sub = 0;
	while (*s1 || *s2)
	{
		sub = *(unsigned char *)s1++ - *(unsigned char *)s2++;
		if (sub != 0)
			return (sub);
	}
	return (0);
}
